// Comparision operators in JavaScript

let a = 10;
let b = 20;

console.log("Is a equal to b?", a == b); // false
console.log("Is a not equal to b?", a != b); // true
console.log("Is a greater than b?", a > b); // false
console.log("Is a less than b?", a < b); // true
console.log("Is a greater than or equal to b?", a >= b); // false
console.log("Is a less than or equal to b?", a <= b); // true
console.log("Is a strictly equal to b?", a === b); // false
console.log("Is a strictly not equal to b?", a !== b); // true
console.log("Is a equal to 10?", a == 10); // true
console.log("Is b greater than or equal to 15?", b >= 15); // true 
console.log("Is b less than or equal to 25?", b <= 25); // true
console.log("Is a strictly equal to '10'?", a === '10'); // false
